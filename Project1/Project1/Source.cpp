#include <iostream>
using namespace std;

int main()
{
    const double millimeters = 1.5;
    double Year_5 = 5 * millimeters;
    double Year_7 = 7 * millimeters;
    double Year_10 = 10 * millimeters;


    cout << "In 5 Years the ocean's level will be higher by" << Year_5 << "  millimeters." << endl;
    cout << "In 7 Years the ocean's level will be higher by" << Year_7 << "  millimeters." << endl;
    cout << "In 10 years the ocean's level will be higher by" << Year_10 << "  millimeters." << endl;

    return 0;
}
